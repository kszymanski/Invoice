package com.invoice.beans.basic;

public class TaxBean {
	private int idTax;
	private String amount;
	private float name;
	
	
	public int getIdTax() {
		return idTax;
	}
	public void setIdTax(int idTax) {
		this.idTax = idTax;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public float getName() {
		return name;
	}
	public void setName(float name) {
		this.name = name;
	}
	
}
